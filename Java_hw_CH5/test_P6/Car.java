
class Car {
	protected int num;
	protected double gas;
	public Car() {
		num = 0;
		gas = 0.0;
	 	System.out.println("生產了車子");
	}

	public void setCar(int n , double g) {
		num = n;
	 	gas  = g;
	 	System.out.println("將產車號為"+num+"，汽油量為"+gas+"的車子");
	}
	public void show() {
		System.out.println("車號是"+num);
	 	System.out.println("汽油量是"+gas);
	}
}
