package pa;

public class Sample_P44 {
	public static void main(String[] args) {
		Car1 car1 = new Car1("1號車");
		car1.start();
		for(int i=0;i<5;i++) {
			System.out.println("正在進行main()的處理工作");
		}
	}
}

class Car1 extends Thread
{
	private String name;
	public Car1(String num) {
		name = num;
	}
	public void run() {
		for(int i=0;i<5;i++) {
			System.out.println("正在進行"+name+"的處理工作");
		}
	}
}
